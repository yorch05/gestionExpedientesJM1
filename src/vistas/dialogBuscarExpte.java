    private void buscarExpediente() {
        ExpedienteFacade expedienteFacade = new ExpedienteFacade();
        expediente = expedienteFacade.getExpediente(jTextField1.getText());
        DefaultListModel listaMenores = new DefaultListModel();
        for (Menor menor : expediente.getMenor()){
            listaMenores.addElement(menor);
        }
        jList1.setModel(listaMenores);
    }

    private void seleccionarMenor() {
        menor = (Menor) jList1.getSelectedValue();
        this.dispose();
    }

    private void habilitarBotonSeleccion() {
        if (!jList1.isSelectionEmpty()) {
            jButton2.setEnabled(true);
        }
    }
