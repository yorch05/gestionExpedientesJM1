    private void buscarMenor() {
        MenorFacade menorFacade = new MenorFacade();
        DefaultListModel modeloLista = new DefaultListModel();
        List<Menor> listaMenores = new ArrayList();
        listaMenores = menorFacade.buscarXApellidoNombre(jTextField1.getText());
        if (listaMenores.size() > 0) {
            try {
                for (Menor menores : listaMenores) {
                    for (int index = 0; index < listaMenores.size(); index++) {
                        modeloLista.addElement(menores);
                    }
                    jList1.setModel(modeloLista);
                }
            } catch (java.lang.ClassCastException ex) {
                JOptionPane.showMessageDialog(rootPane, "No existe el menor buscado. Verifique datos", "Resultado", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "No se encontrÃ³ el menor. Verifique datos", "Resultado", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
